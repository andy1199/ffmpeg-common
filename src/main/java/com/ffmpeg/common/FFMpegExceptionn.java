public class FFMpegExceptionn extends RuntimeException {

    public FFMpegExceptionn() {
        super();
    }

    public FFMpegExceptionn(String message, Throwable cause) {
        super(message, cause);
    }

    public FFMpegExceptionn(String message) {
        super(message);
    }

    public FFMpegExceptionn(Throwable cause) {
        super(cause);
    }

}
